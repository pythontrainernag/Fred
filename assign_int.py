# Assignment operators
# =, +=, -=, *= ----> similarly with every arthematic operators
# Assignment operators are used for assigning a value/values to a variable
# This operators can't be used along with print statements

a = 5 # The value(5) will be assigned to a valriable "a"
print(a)#As "a" value is 5, it prints 5
#print(a=5)

a += 10 # a = a+10 Always right side of the = is executed first and result is assigned to variable on left side
print(a)

a *= 4 # a = a*4
print(a)

# modulos is nothing but a remainder
a %= 8# a = a%8 ---> 60%8
